#math is done the same way as CSS using PEMDAS 

#to convert a string to an int or a float
int("11")
float("11")
#you can also convert an int to a float like so and it will display as a float
float(11)
#you can also do the opposite and convert a float to an int
int(11.0) #this will forget everything after the decimal which is handy if you only want to work with int

#you can also do this with a division shortcut
#so
23/3 #will give you a decimal
#and 
23//3 #will give you an int
#modulos % still work the same as they give the remainder so
23%3 #will give back 7 as there is a remainder of 7